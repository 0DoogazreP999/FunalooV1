import type { GenerationIntelligenceProfile } from "@/lib/engine/types"

export interface GeneratedCodeContext {
  originalPrompt?: string
  assignedAgent?: string
  assignedAgentSystemPrompt?: string
  generatedSystems?: string[]
  verificationChecklist?: string[]
}

function toClassStem(feature: string) {
  return feature
    .split("_")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("")
}

function designHeader(design?: GenerationIntelligenceProfile, context?: GeneratedCodeContext) {
  if (!design) return []

  return [
    ...(context?.originalPrompt ? [`// Original Prompt: ${context.originalPrompt}`] : []),
    ...(context?.assignedAgent ? [`// Assigned Agent: ${context.assignedAgent}`] : []),
    ...(context?.assignedAgentSystemPrompt ? [`// Agent Focus: ${context.assignedAgentSystemPrompt}`] : []),
    `// Dimension: ${design.dimension.toUpperCase()}`,
    `// Pitch: ${design.generatedPitch}`,
    `// Player Fantasy: ${design.playerFantasy}`,
    `// Camera: ${design.cameraStyle}`,
    `// Render Path: ${design.graphicsPlan.renderPath}`,
    `// Engine Recommendation: ${design.enginePlan.recommendedEngine}`,
    `// Map Archetype: ${design.mapArchetype}`,
    `// Non-Overlap Strategy: ${design.nonOverlapStrategy}`,
    `// Gameplay Loop: ${design.gameplayLoopSummary}`,
    `// Core Loop Beats: ${design.coreLoop.join(" -> ")}`,
    `// Content Pillars: ${design.contentPillars.join(", ")}`,
    `// Complementary Systems: ${design.complementarySystems.join(", ")}`,
    ...(context?.generatedSystems?.length ? [`// Existing Systems: ${context.generatedSystems.join(", ")}`] : []),
    ...(context?.verificationChecklist?.length ? [`// Verification Focus: ${context.verificationChecklist.join(" | ")}`] : []),
  ]
}

function emitRepeatedBodies(
  out: string[],
  className: string,
  methodPrefix: string,
  iterations: number,
  bodyLines: (index: number) => string[],
) {
  for (let index = 0; index < iterations; index += 1) {
    out.push(`void ${className}::${methodPrefix}_${index}() {`)
    bodyLines(index).forEach((line) => out.push(`    ${line}`))
    out.push("}")
    out.push("")
  }
}

function layoutRuleComment(design?: GenerationIntelligenceProfile) {
  return design?.layoutRules.length ? design.layoutRules[0] : "Maintain readable traversal and conflict-free spatial ownership."
}

function emitEngineHeader(
  out: string[],
  engine: string,
  feature: string,
  genre: string,
  className: string,
  design?: GenerationIntelligenceProfile,
  context?: GeneratedCodeContext,
) {
  const commentPrefix = engine === "godot" ? "#" : "//"

  out.push(`${commentPrefix} Generated by NEXUS-116 AI Fleet`)
  out.push(`${commentPrefix} Feature: ${feature.replace(/_/g, " ")}`)
  out.push(`${commentPrefix} Engine: ${engine}`)
  out.push(`${commentPrefix} Genre: ${genre}`)
  designHeader(design, context).forEach((line) => out.push(engine === "godot" ? line.replace(/^\/\//, "#") : line))
  out.push(`${commentPrefix} Optimized for Production`)
  out.push("")

  if (engine === "godot") {
    out.push("class_name " + className.replace(/^U/, ""))
    out.push("extends Node")
    out.push("")
    out.push(`## ${feature.replace(/_/g, " ")} system for Godot 4`)
    out.push(`## Uses GDExtension-compatible patterns and signal-driven architecture`)
    out.push("")
  } else if (engine === "unity") {
    out.push("using UnityEngine;")
    out.push("using System.Collections.Generic;")
    out.push("")
    out.push(`namespace Nexus.${toClassStem(feature)} {`)
    out.push("")
    out.push("/// <summary>")
    out.push(`/// ${feature.replace(/_/g, " ")} system for Unity — MonoBehaviour-based with serialized fields`)
    out.push("/// </summary>")
  } else if (engine === "custom") {
    out.push("#pragma once")
    out.push("#include <cstdint>")
    out.push("#include <vector>")
    out.push("#include <string>")
    out.push("#include <memory>")
    out.push("#include <functional>")
    out.push("")
    out.push(`// Custom C++ engine — no framework dependencies, pure ECS/DOD patterns`)
    out.push(`namespace nexus::${feature} {`)
    out.push("")
  } else {
    out.push("#pragma once")
    out.push('#include "CoreMinimal.h"')
    out.push('#include "GameFramework/Actor.h"')
    out.push('#include "Components/ActorComponent.h"')
    out.push(`#include "${className}.generated.h"`)
  }

  out.push("")
}

function emitEngineFooter(out: string[], engine: string) {
  if (engine === "unity") {
    out.push("}")
    out.push("// end namespace")
  } else if (engine === "custom") {
    out.push("}")
    out.push("// end namespace")
  }
}

